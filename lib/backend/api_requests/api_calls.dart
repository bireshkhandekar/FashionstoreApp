class AppApisGroup {
  static String getBaseUrl() => 'http://api.rajastechnologies.com:5001/';
  static Map<String, String> headers = {};
  static GetProductsCall getProductsCall = GetProductsCall();
}

class GetProductsCall {
  Future<ApiCallResponse> call({
    int? limit,
    int? page,
  }) async {
    final baseUrl = AppApisGroup.getBaseUrl();

    return ApiManager.instance.makeApiCall(
      callName: 'get products',
      apiUrl: '$baseUrl/items?limit=$limit&page=$page',
      callType: ApiCallType.GET,
      headers: {},
      params: {},
      returnBody: true,
      encodeBodyUtf8: false,
      decodeUtf8: false,
      cache: false,
      isStreamingApi: false,
      alwaysAllowBody: false,
    );
  }

  List? products(dynamic response) => getJsonField(
        response,
        r'''$.data''',
        true,
      ) as List?;
}

